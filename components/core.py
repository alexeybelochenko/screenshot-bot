def clear_assets():
    try:
        folder = 'components/assets'
        for filename in os.listdir(folder):
            file_path = os.path.join(folder, filename)
            try:
                if os.path.isfile(file_path) or os.path.islink(file_path):
                    os.unlink(file_path)
                elif os.path.isdir(file_path):
                    shutil.rmtree(file_path)
            except Exception as e:
                print('Failed to delete %s. Reason: %s' % (file_path, e))
    except:
        os.mkdir('components/assets')


def url_validation(url):
    return re.findall(URL_REGEX, url)


def convert_url(url):
    if not re.match('(?:http|ftp|https)://', url):
        return 'https://{}'.format(url)
    return url


def static_messages():
    with open('components/static_messages.json') as f:
        messages_store = json.load(f)
        return messages_store


def take_screenshot(url):
    chrome_options = Options()
    chrome_options.add_argument('--headless')
    chrome_options.add_argument('--start-maximized')
    DRIVER = 'chromedriver'
    driver = webdriver.Chrome(chrome_options=chrome_options, executable_path=DRIVER)
    driver.get(convert_url(url.lower()))
    time.sleep(0.5)
    # data = lambda X: driver.execute_script('return document.body.parentNode.scroll' + X)
    # driver.set_window_size(1920, data('Height'))
    driver.find_element_by_tag_name('body')
    driver.set_window_size(1920, 1080)
    time.sleep(1.5)
    file_name = f'site-{random.randint(0, 1000000)}.png'
    driver.save_screenshot(f'components/assets/{file_name}')
    driver.quit()

    return file_name
